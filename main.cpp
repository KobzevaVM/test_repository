#include <iostream>
using namespace std;
int main(int argc, const char * argv[]) {
    int a, b;
    cout << "введите число а" << endl;
    cin >> a;
    cout << "введите число b" << endl;
    cin >> b;
    cout << "сумма a и b: ";
    cout << a + b << endl;
    int c;
    cin >> c >> endl;
    cout << "сумма a и c: ";
    cout << a + c << endl;
    return 0;
}
